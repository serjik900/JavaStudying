package com.syntax.class08;

public class IncrementAndDecreament {

	public static void main(String[] args) {
		int num=90;
		num++; //+1
		System.out.println(num);
		//decrement a value by 1 ->--
		num--;
		System.out.println(num);//91
		
		int i=10;
		i--;
		i--;
		System.out.println(i);//8
	}

}
